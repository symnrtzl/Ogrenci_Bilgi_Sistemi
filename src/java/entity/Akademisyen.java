package entity;

/**
 *
 * @author Lenovo
 */
public class Akademisyen {
    
    private int id;
    private String ad;
    private String email;
    private int numara;
    private Bolum bolum;

    public Akademisyen() {
    }

    public Akademisyen(int id, String ad, String email, int numara, Bolum bolum) {
        this.id = id;
        this.ad = ad;
        this.email = email;
        this.numara = numara;
        this.bolum = bolum;
    }

   

    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getNumara() {
        return numara;
    }

    public void setNumara(int numara) {
        this.numara = numara;
    }

    public Bolum getBolum() {
        return bolum;
    }

    public void setBolum(Bolum bolum) {
        this.bolum = bolum;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 29 * hash + this.id;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Akademisyen other = (Akademisyen) obj;
        return this.id == other.id;
    }

    
    
    
}
